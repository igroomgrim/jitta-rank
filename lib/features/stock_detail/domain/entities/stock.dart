class Stock {
  final String id;
  final int stockId;
  final String currency;
  final String currencySign;
  final String title;
  final String symbol;
  final String summary;

  Stock({
    required this.id,
    required this.stockId,
    required this.currency,
    required this.currencySign,
    required this.title,
    required this.symbol,
    required this.summary,
  });
}

